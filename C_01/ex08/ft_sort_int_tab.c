void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_sort_int_tab(int *tab, int size)
{
	int	read_index;
	int	index;
	int	swapped;

	read_index = 0;
	while (true)
	{
		swapped = 0;
		index = 0;
		while (index < size)
		{
			if (tab[index] > tab[index + 1])
			{
				ft_swap(&tab[index], &tab[index + 1]);
				swapped = 1;
			}
			index++;
		}
		if (!swapped)
			break ;
		read_index++;
	}
}
